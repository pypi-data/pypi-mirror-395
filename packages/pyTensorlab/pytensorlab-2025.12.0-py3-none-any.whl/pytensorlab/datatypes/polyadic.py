@_mtkrprod_all.register
def _mtkrprod_all_polyadic(
    T: PolyadicTensor, U: Sequence[MatrixType], conjugate: bool = True
) -> Sequence[MatrixType]:
    V = T.factors
    VU = [v.T @ u.conj() for (v, u) in zip(V, U)]
